const dictionaries: Record<Locale, Record<MessageKey, string>> = {
  "zh-CN": zh,
  en,
};

export const localeStorageKey = "pinshift_locale_v1";

export function readLocale(): Locale {
  try {
    const saved = window.localStorage.getItem(localeStorageKey);
    if (saved === "zh-CN" || saved === "en") return saved;
  } catch {
    // Browser language remains a safe fallback when storage is unavailable.
  }
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function translate(locale: Locale, key: MessageKey, values: MessageValues = {}) {
  return dictionaries[locale][key].replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = values[name];
    return value === undefined ? match : String(value);
  });
}
